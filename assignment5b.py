def main():
    g = int(input("Enter your grade(between 1 and 100): "))
    if g >=90:
        print("Your grade is A")

    elif g >=80 and g<90:
         print("Your grade is B")

    elif g >= 70 and g<80:
         print("Your score is C")

    elif g >=60 and g<70:
         print("Your grade is D")

    elif g >=50 and g<60:
         print("Your grade is E")

    elif g >=1 and g<60:
         print("Your grade is F")

main()
       
